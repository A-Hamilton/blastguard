#!/usr/bin/env bash
# bench/scripts/clone_sweagent.sh
#
# Clones SWE-agent v1.1.0 into bench/.sweagent-repo so the installed Python
# package can find its required config/, tools/, and trajectories/ directories.
#
# The sweagent package on PyPI (0.0.1) is a broken stub. We install from git
# via [tool.uv.sources], but the package expects config/tools/trajectories to
# live at the repo root (PACKAGE_DIR.parent). Those dirs are not shipped in
# the wheel. Solution: clone the repo here and point the env vars at it.
#
# After running this, export:
#   SWE_AGENT_CONFIG_DIR="$(pwd)/.sweagent-repo/config"
#   SWE_AGENT_TOOLS_DIR="$(pwd)/.sweagent-repo/tools"
#   SWE_AGENT_TRAJECTORY_DIR="$(pwd)/.sweagent-repo/trajectories"
#
# Or source bench/scripts/sweagent_env.sh (created by this script).
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
BENCH_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
REPO_DIR="$BENCH_DIR/.sweagent-repo"
VERSION="v1.1.0"

if [[ -d "$REPO_DIR/.git" ]]; then
    echo "bench/.sweagent-repo already exists — skipping clone."
else
    echo "Cloning SWE-agent $VERSION into $REPO_DIR ..."
    git clone --depth=1 --branch "$VERSION" \
        https://github.com/SWE-agent/SWE-agent.git \
        "$REPO_DIR"
    echo "Clone complete."
fi

# Emit an env file that can be sourced.
ENV_FILE="$BENCH_DIR/scripts/sweagent_env.sh"
cat > "$ENV_FILE" <<EOF
# Auto-generated by clone_sweagent.sh — source this before running sweagent.
export SWE_AGENT_CONFIG_DIR="$REPO_DIR/config"
export SWE_AGENT_TOOLS_DIR="$REPO_DIR/tools"
export SWE_AGENT_TRAJECTORY_DIR="$REPO_DIR/trajectories"
EOF

echo "Wrote env file: $ENV_FILE"
echo "Run: source $ENV_FILE"
